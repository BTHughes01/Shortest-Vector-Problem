#ifndef PRINT_FUNCTIONS_H
#define PRINT_FUNCTIONS_H

/**
 * @brief Prints the content of a matrix.
 *
 * This function prints the content of the given matrix.
 *
 * @param matrix The matrix to be printed.
 * @param dimension The dimension of the matrix.
 */
void print_matrix(double** matrix, int dimension);

/**
 * @brief Prints the content of a vector.
 *
 * This function prints the content of the given vector.
 *
 * @param vector The vector to be printed.
 * @param dimension The dimension of the vector.
 */
void print_vector(double* vector, int dimension);

#endif /* PRINT_FUNCTIONS_H */
